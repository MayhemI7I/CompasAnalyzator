package main

import (
	"compass_analyzer/analyzer"
)

func main() {
	// Тестовые данные из компаса 2084 (записи 92-115)
	angles := []float64{
		127, 128, 128, 128, 126, 127, // Первый стабильный сегмент
		218, 219, 220, 221, // Поворот на ~90 градусов
		316, 315, 316, 315, 322, // Поворот на ~90 градусов (322 - промежуточное значение)
		37, 36, 36, 37, // Поворот на ~90 градусов
		95,                 // Промежуточное значение
		127, 126, 127, 129, // Поворот на ~90 градусов
	}

	// Анализируем данные
	turns := analyzer.AnalyzeCompassData(angles)

	// Выводим результаты
	analyzer.PrintAnalysis(angles, turns)
}
